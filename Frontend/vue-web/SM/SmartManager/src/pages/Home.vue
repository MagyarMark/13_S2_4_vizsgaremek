<script>
window.addEventListener('load', () => {
  setTimeout(() => {
    const loader = document.getElementById('loader');
    if (!loader) return;
    loader.classList.add('fade-out');
    setTimeout(() => {
      loader.remove();
    }, 700);
  }, 1500);
});

const menuBtn = document.getElementById('menuToggle');
const mainNav = document.getElementById('mainNav');
const headerEl = document.querySelector('header');

function toggleMobileNav() {
  if (!mainNav) return;
  mainNav.classList.toggle('active');
}

if (menuBtn) menuBtn.addEventListener('click', toggleMobileNav);

function onScroll() {
  if (!headerEl) return;
  headerEl.style.background = window.scrollY > 50
    ? 'rgba(8, 24, 39, 0.95)'
    : 'rgba(8, 24, 39, 0.85)';
}

window.addEventListener('scroll', onScroll);

// Cleanup listeners when the page is unloaded/reloaded
window.addEventListener('beforeunload', () => {
  if (menuBtn) menuBtn.removeEventListener('click', toggleMobileNav);
  window.removeEventListener('scroll', onScroll);
});

export default {
  name: "Home",
  data() {
    return {
      navActive: false,
    }
  },
  methods: {
    toggleMenu() {
      this.navActive = !this.navActive;
    }
  }
}
